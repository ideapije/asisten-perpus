<h1> Deskripsi tentang Sistem </h1>
<p><strong>Digital Assistant Library</strong>
<i>Digital Assistant Library</i> merupakan sistem peminjaman buku berbasis RFID (<i>Radio Frequency Identification</i>) yang memberikan kemudahan kepada pengunjung perpustakaan dalam mencari ketersediaan buku dan meminjam buku secara <i>self-service</i>. Sistem ini juga menyediakan layanan <i>booking</i> buku secara <i>online</i> saat peminjam berada jauh dari perpustakaan. Pada saat pengembalian peminjam tidak perlu khawatir terlambat mengembalikan buku karena akan ada sms notifikasi dari perpustakaan bahwa masa pinjam buku sudah berakhir.
</p>
<h1>Aturan Peminjaman</h1>
<ul>
<li>Peminjam merupakan anggota perpustakaan</li>
<li>Masa aktif kartu anggota hanya berlaku selama yang bersangkutan menjadi mahasiswa atau dosen aktif di STT Telematika Telkom Purwokerto</li>
<li>Maksimal buku yang dapat dipinjam sebanyak dua buah per orang</li>
<li>Batas waktu peminjaman buku selama tiga hari</li>
<li> Peminjaman dan pengembalian buku tidak dapat diwakilkan</li>
<li> Denda keterlambatan dibayarkan pada admin saat mengembalikan buku</li>
<li> Buku yang hilang atau rusak maka peminjam harus menganti buku yang serupa atau membayar denda sesuai dengan harga buku tersebut</li>
<li> Kartu anggota dan RFID tag rusak atau hilang maka akan dikenakan denda sebesar Rp 50.000,-</li>
</ul>
<h1>Petunjuk Peminjaman Buku</h1>
<ul>
<li>Cari judul buku pada komputer pelayanan, maka ketersediaan dan letak buku akan ditampilkan</li>
<li>Ambil buku tersebut pada rak sesuai dengan data yang ditampilkan</li>
<li>Bawa buku pada komputer peminjaman</li> 
<li>Letakkan kartu anggota di atas RFID <i>reader</i></li>
<li>Letakkan buku yang akan dipinjam di atas RFID <i>reader</i></li>
<li>Buku tersebut akan ditampilkan pada layar komputer dan akan muncul notifikasi peminjaman</li>
<li>Maka akan muncul jendela informasi dan klik pinjam</li>
</ul>
<h1>Petunjuk <i>Booking</i> Buku</h1>
<ul>
<li>Login pada website <i>Digital Assistant Library</i></li>
<li>Cari buku yang akan dipinjam pada kolom pencarian</li>
<li>Akan muncul jendela informasi mengenai ketersediaan buku yang dicari</li>
<li>Klik <i>booking</i>  pada jendela informasi tersebut</li>
<li>Masa <i>booking</i> hanya 1 x 24 jam</li>
<li>Ambil buku tersebut di perpustakaan</li>
</ul>
<h1>Petunjuk Pengembalian Buku</h1>
<ul>
<li>Peminjam akan menerima sms notifikasi jika masa pinjam sudah berakhir</li>
<li>Peminjam harus mengembalikan buku ke perpustakaan</li>
<li>Letakkan buku dan kartu anggota di atas  RFID <i>reader</i></li>
<li>Akan muncul  jendela notifikasi dan klik menu kembalikan</li>
<li>Serahkan buku pada  tempat yang telah disediakan</li>
<li>Bayar denda ke admin jika terlambat mengembalikan buku</li>
</ul>
